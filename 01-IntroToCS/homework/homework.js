function BinarioADecimal(num) {
   // 1100101
   let decimal=0;
   for (let i = 0; i<num.length; i++) {
      if (num[i] == 1){
         decimal =decimal +(2**(num.length-1-i))
      }
   }
   return decimal;
   
}

console.log(BinarioADecimal('10'));


function DecimalABinario(num) {
   let div = num;
   let binario =[]
   while (div>=1 ) {
      let res = div%2;
      if (res==1) binario.unshift(1); 
      else binario.unshift(0);
      div = ~~(div/2);
   }
   return binario.join('');

}
console.log(DecimalABinario(7));

